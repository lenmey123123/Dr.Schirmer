import { type ClassValue, clsx } from 'clsx';
import { twMerge } from 'tailwind-merge';

/**
 * Utility function to merge Tailwind CSS classes
 * Combines clsx and tailwind-merge for optimal class handling
 */
export function cn(...inputs: ClassValue[]) {
  return twMerge(clsx(inputs));
}

/**
 * Format a date to German locale
 */
export function formatDate(date: Date | string | number): string {
  const dateObj = new Date(date);
  return dateObj.toLocaleDateString('de-DE', {
    year: 'numeric',
    month: 'long',
    day: 'numeric',
  });
}

/**
 * Format a date to relative time (e.g., "vor 2 Stunden")
 */
export function formatRelativeTime(date: Date | string | number): string {
  const dateObj = new Date(date);
  const now = new Date();
  const diffInSeconds = Math.floor((now.getTime() - dateObj.getTime()) / 1000);

  if (diffInSeconds < 60) {
    return 'vor einem Moment';
  }

  const diffInMinutes = Math.floor(diffInSeconds / 60);
  if (diffInMinutes < 60) {
    return `vor ${diffInMinutes} Minute${diffInMinutes > 1 ? 'n' : ''}`;
  }

  const diffInHours = Math.floor(diffInMinutes / 60);
  if (diffInHours < 24) {
    return `vor ${diffInHours} Stunde${diffInHours > 1 ? 'n' : ''}`;
  }

  const diffInDays = Math.floor(diffInHours / 24);
  if (diffInDays < 7) {
    return `vor ${diffInDays} Tag${diffInDays > 1 ? 'en' : ''}`;
  }

  const diffInWeeks = Math.floor(diffInDays / 7);
  if (diffInWeeks < 4) {
    return `vor ${diffInWeeks} Woche${diffInWeeks > 1 ? 'n' : ''}`;
  }

  const diffInMonths = Math.floor(diffInDays / 30);
  if (diffInMonths < 12) {
    return `vor ${diffInMonths} Monat${diffInMonths > 1 ? 'en' : ''}`;
  }

  const diffInYears = Math.floor(diffInDays / 365);
  return `vor ${diffInYears} Jahr${diffInYears > 1 ? 'en' : ''}`;
}

/**
 * Format a phone number to German format
 */
export function formatPhoneNumber(phone: string): string {
  // Remove all non-digit characters
  const cleaned = phone.replace(/\D/g, '');
  
  // German phone number formatting
  if (cleaned.startsWith('49')) {
    // International format: +49 123 456789
    const countryCode = cleaned.slice(0, 2);
    const areaCode = cleaned.slice(2, 5);
    const number = cleaned.slice(5);
    return `+${countryCode} ${areaCode} ${number}`;
  } else if (cleaned.startsWith('0')) {
    // National format: 0123 456789
    const areaCode = cleaned.slice(0, 4);
    const number = cleaned.slice(4);
    return `${areaCode} ${number}`;
  }
  
  return phone; // Return original if format is not recognized
}

